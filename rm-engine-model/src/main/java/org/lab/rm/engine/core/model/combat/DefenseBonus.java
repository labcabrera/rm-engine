package org.lab.rm.engine.core.model.combat;

public class DefenseBonus {

	private Integer armorQuality;

	private Integer quickness;

	private Integer adrenalineDefense;

	private Integer positionBonus;

	private Integer cobertureBonus;

	private Integer magicBonus;

	private Integer escudo;

	private Integer parada;

	public Integer getArmorQuality() {
		return armorQuality;
	}

	public void setArmorQuality(Integer armorQuality) {
		this.armorQuality = armorQuality;
	}

	public Integer getQuickness() {
		return quickness;
	}

	public void setQuickness(Integer quickness) {
		this.quickness = quickness;
	}

	public Integer getAdrenalineDefense() {
		return adrenalineDefense;
	}

	public void setAdrenalineDefense(Integer adrenalineDefense) {
		this.adrenalineDefense = adrenalineDefense;
	}

	public Integer getPositionBonus() {
		return positionBonus;
	}

	public void setPositionBonus(Integer positionBonus) {
		this.positionBonus = positionBonus;
	}

	public Integer getCobertureBonus() {
		return cobertureBonus;
	}

	public void setCobertureBonus(Integer cobertureBonus) {
		this.cobertureBonus = cobertureBonus;
	}

	public Integer getMagicBonus() {
		return magicBonus;
	}

	public void setMagicBonus(Integer magicBonus) {
		this.magicBonus = magicBonus;
	}

	public Integer getEscudo() {
		return escudo;
	}

	public void setEscudo(Integer escudo) {
		this.escudo = escudo;
	}

	public Integer getParada() {
		return parada;
	}

	public void setParada(Integer parada) {
		this.parada = parada;
	}
}
