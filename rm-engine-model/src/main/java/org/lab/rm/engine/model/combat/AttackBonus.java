package org.lab.rm.engine.model.combat;

public class AttackBonus {

	private Integer skill;

	private Integer attribute;

	private Integer level;

	private Integer weaponQuality;

	private Integer magicBonus;

	private Integer positionBonus;

	private Integer relativeBonus;

	private Integer parada;

	private Integer leftWeapon;

	private Integer rechargeBonus;

	private Integer projectileArmorBonus;

	public Integer getSkill() {
		return skill;
	}

	public void setSkill(Integer skill) {
		this.skill = skill;
	}

	public Integer getAttribute() {
		return attribute;
	}

	public void setAttribute(Integer attribute) {
		this.attribute = attribute;
	}

	public Integer getLevel() {
		return level;
	}

	public void setLevel(Integer level) {
		this.level = level;
	}

	public Integer getWeaponQuality() {
		return weaponQuality;
	}

	public void setWeaponQuality(Integer weaponQuality) {
		this.weaponQuality = weaponQuality;
	}

	public Integer getMagicBonus() {
		return magicBonus;
	}

	public void setMagicBonus(Integer magicBonus) {
		this.magicBonus = magicBonus;
	}

	public Integer getPositionBonus() {
		return positionBonus;
	}

	public void setPositionBonus(Integer positionBonus) {
		this.positionBonus = positionBonus;
	}

	public Integer getRelativeBonus() {
		return relativeBonus;
	}

	public void setRelativeBonus(Integer relativeBonus) {
		this.relativeBonus = relativeBonus;
	}

	public Integer getParada() {
		return parada;
	}

	public void setParada(Integer parada) {
		this.parada = parada;
	}

	public Integer getLeftWeapon() {
		return leftWeapon;
	}

	public void setLeftWeapon(Integer leftWeapon) {
		this.leftWeapon = leftWeapon;
	}

	public Integer getRechargeBonus() {
		return rechargeBonus;
	}

	public void setRechargeBonus(Integer rechargeBonus) {
		this.rechargeBonus = rechargeBonus;
	}

	public Integer getProjectileArmorBonus() {
		return projectileArmorBonus;
	}

	public void setProjectileArmorBonus(Integer projectileArmorBonus) {
		this.projectileArmorBonus = projectileArmorBonus;
	}

}
